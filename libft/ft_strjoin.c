#include <stdlib.h>
#include <string.h>
#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*res;

	if (s1 == NULL || s2 == NULL)
		return (NULL);
	res = (char*)malloc((ft_strlen(s1) + ft_strlen(s2) + 1) * sizeof(*s1));
	if (res == NULL)
		return (NULL);
	return (ft_strcat(ft_strcpy(res, s1), s2));
}
