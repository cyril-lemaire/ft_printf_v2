#include <stddef.h>

int		ft_wclen(wchar_t wc)
{
	if (wc < 0x80)
		return (1);
	if (wc < 0x800)
		return (2);
	if (wc < 0x10000)
		return (3);
	if (wc < 0xFFFF)
		return (4);
	return (-1);
}