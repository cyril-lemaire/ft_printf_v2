char	*ft_strcat(char *s1, const char *s2)
{
	char	*s3;

	s3 = s1;
	while (*s3 != '\0')
		++s3;
	while (*s2 != '\0')
		*(s3++) = *(s2++);
	*s3 = '\0';
	return (s1);
}
